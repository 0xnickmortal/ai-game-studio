/** Get agents by tier */
export function getAgentsByTier(tier: AgentTier): AgentDef[] {
  return AGENTS.filter(a => a.tier === tier);
}

/** Get agent by ID */
export function getAgentById(id: string): AgentDef | undefined {
  return AGENTS.find(a => a.id === id);
}

/** Get all agents in a domain */
export function getAgentsByDomain(domain: string): AgentDef[] {
  return AGENTS.filter(a => a.domain === domain);
}

/** Get agent's subordinates */
export function getSubordinates(agentId: string): AgentDef[] {
  const agent = getAgentById(agentId);
  if (!agent) return [];
  return agent.delegatesTo.map(id => getAgentById(id)).filter(Boolean) as AgentDef[];
}

/** Get agents filtered by engine (returns universal + engine-specific) */
export function getAgentsByEngine(engine: GameEngine | null): AgentDef[] {
  if (!engine) return AGENTS.filter(a => !a.engine);
  return AGENTS.filter(a => !a.engine || a.engine === engine);
}

/** Get only engine-specific agents */
export function getEngineAgents(engine: GameEngine): AgentDef[] {
  return AGENTS.filter(a => a.engine === engine);
}

/** Count agents by category */
export function getAgentStats() {
  return {
    total: AGENTS.length,
    tier1: getAgentsByTier(1).length,
    tier2: getAgentsByTier(2).length,
    tier3: getAgentsByTier(3).length,
    universal: AGENTS.filter(a => !a.engine).length,
    godot: AGENTS.filter(a => a.engine === 'godot').length,
    unity: AGENTS.filter(a => a.engine === 'unity').length,
    unreal: AGENTS.filter(a => a.engine === 'unreal').length,
  };
}
